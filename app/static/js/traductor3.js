window.t = function(clave) {
    const idioma = localStorage.getItem("vitaria_idioma") || "es";
    
    if (traducciones[idioma] && traducciones[idioma][clave]) {
        return traducciones[idioma][clave];
    }
    
    console.warn('Traducción no encontrada:', clave, 'para idioma:', idioma);
    return clave;
};

window.CambiarIdioma = function(idioma){
    document.querySelectorAll("[lan-target]").forEach(el =>{
        const clave = el.getAttribute("lan-target");
        const texto = traducciones[idioma]?.[clave];
        if (texto) el.textContent = texto;
    });
    
    document.querySelectorAll("[lan-placeholder]").forEach(el => {
        const clave = el.getAttribute("lan-placeholder");
        const texto = traducciones[idioma]?.[clave];
        if (texto) el.placeholder = texto;
    });

    localStorage.setItem('vitaria_idioma', idioma);
    document.querySelectorAll('.btn-lang').forEach(btn => btn.classList.remove('active'));
    const btnActivo = document.getElementById(`btn-${idioma}`);
    if (btnActivo) btnActivo.classList.add('active');
};

document.addEventListener('DOMContentLoaded', () => {
    const idiomaGuardado = localStorage.getItem('vitaria_idioma') || 'es';
    window.CambiarIdioma(idiomaGuardado);
});